
CREATE TABLE pde.pde_session_stage
(
    Session_id varchar(50),
    Session_StartTime timestamp,
    Session_EndTime timestamp,
    Duration float,
    isAlmanac boolean,
    isEph boolean,
    sitename varchar(50),
    createdDate timestamp NOT NULL DEFAULT "sysdate"()
)
PARTITION BY (((date_part('day', pde_session_stage.createdDate) + (date_part('month', pde_session_stage.createdDate) * 100)) + (date_part('year', pde_session_stage.createdDate) * 10000)));


